int write_test_sd(void) {
    int ok = 0;
    if (f_mount(&fs, "/", 1) == FR_OK) {
        if (f_open(&file, "test.txt", FA_WRITE | FA_CREATE_ALWAYS) == FR_OK) {
            char text[] = "Hello World!\n";
            if (f_write(&file, text, sizeof(text), &bytesWritten) == FR_OK) {
                ok = 1;
            }
            f_close(&file);
        }
        f_mount(NULL, "/", 0);
    }
    return ok;
}

void blink_led(int led, int times) {
    for (int i = 0; i < times; i++) {
        BSP_LED_On(led);
        HAL_Delay(100);
        BSP_LED_Off(led);
        HAL_Delay(100);
    }
    BSP_LED_On(led);
}
